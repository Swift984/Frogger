
public class Frog {
		public boolean living;
		public int frogX, frogY;
		
		
		
		public int getX()
		{
			return frogX;
		}
		

		public void changeX(int x)
		{
			frogX = x;
		}

		public int getY()
		{
			return frogY;
		}
		

		public void changeY(int y)
		{
			frogY = y;
		}

		
		
}
